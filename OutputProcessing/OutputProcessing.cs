    public class OutputProcessing
    {
        
        private string Object { get; set; }
        private VariableNameType VariableName { get; set; }
        public int? NumberofVariables { get; set; }


        public OutputProcessing()
        {

        }

        List<OutputProcessing> outputProcessingList = new List<OutputProcessing>();
        public void Add(VariableNameType variableName, string eobject)
        {

            outputProcessingList.Add(new OutputProcessing { VariableName = variableName, Object = eobject });
        }

        public string[] Read()
        {
            string[] read = new string[outputProcessingList.Count];
            for (int i = 0; i < outputProcessingList.Count; i++)
            {
                read[i] = $"Variable {outputProcessingList[i].VariableName} for the Element {outputProcessingList[i].Object} --> Index:{i + 1} ";
                
            }
            return read;
        }
        public string GetOutputVariables()
        {
            return $"Obect: {Object}, Variable: {VariableName}";
        }

    }
